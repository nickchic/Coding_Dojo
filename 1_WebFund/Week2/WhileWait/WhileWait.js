var daysUntilBDay = 60;

while(daysUntilBDay > 0){

    if(daysUntilBDay > 30){

        console.log("Aww man its still " + daysUntilBDay + " days until my birthday");

    } else if (daysUntilBDay > 10){

        console.log("Sweet! its " + daysUntilBDay + " days until my birthday");

    } else {

        console.log("OMG My Birthday is only " + daysUntilBDay + " days away");

    }


    console.log();

    daysUntilBDay--;

}

console.log("HAPPY BIRTHDAY!!!!");
